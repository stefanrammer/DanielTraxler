            	<h2>Werdegang</h2>
             	<p>Meine Leidenschaft zum Skifahren entdeckte ich schon als ich noch ein kleines Kind war. Mit 3 Jahren stand ich das erste Mal auf Skiern und nahm im Alter von 6 Jahren an den ersten Rennen teil. </p>
<p>Da mir das Rennfahren großen Spaß bereitete, entschied ich mich, in die Skihauptschule Windischgarsten zu gehen. Ich fuhr damals meine ersten nationalen Rennen, zu dieser Zeit noch als alpiner Rennläufer. Nach 4 tollen Jahren wechselte ich in die Skihandelsschule Schladming. Mit 15 Jahren bestritt ich meine ersten FIS-Rennen. Vor allem Abfahrt und Super-G waren meine Paradedisziplinen, in denen ich auch meine ersten Europacuprennen bestritt.  Nach einem schweren Sturz im Abfahrtstraining der Österreichischen Meisterschaften in Saalbach musste ich eine Entscheidung für meine weitere sportliche Zukunft treffen. </p>
<p>Zur gleichen Zeit wurde in der Ski-Akademie Schladming die Trendsportart Skicross als neue Sparte eingeführt. Ich wagte den Wechsel und fühlte mich gleich sehr wohl. Auch meine ersten Ergebnisse stimmten mich positiv. Aufgrund der Erfolge in einigen Europacup- und FIS-Rennen qualifizierte ich mich 2013 und 2014 für die Junioren-Weltmeisterschaft. </p>
<p>In der Saison 2013/14 wurde ich in den ÖSV-B-Kader aufgenommen. Ein Jahr später konnte ich mein erstes Europacuprennen gewinnen und erreichte in der Europacupgesamtwertung den 2. Platz. Auf Grund dessen stieg ich in den ÖSV-A-Kader auf. </p>
<p>Dieses Jahr konnte ich in meiner ersten vollen Weltcupsaison mit einem 5. Platz bei der Olympiageneralprobe für 2018 in Südkorea mein bisher bestes Weltcupergebnis erzielen. Mit weiteren starken Platzierungen erreichte ich dadurch den 27. Platz in der Weltcupgesamtwertung. Beim Saisonfinale wurde ich von allen Nationaltrainern zum „Rookie of the year“ gewählt, eine Auszeichnung für den besten Aufsteiger der Saison. </p>
